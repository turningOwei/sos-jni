package sos_jni.domain;

public class Device {
	private String deviceId;
	
	private String deviceIp;
	
	private String deviceType;
	
	private String deviceStatus;
	
	private String deviceVolume;
	
	private String deviceOtherInfo;
	
	public String getDeviceId() {
		return deviceId;
	}
	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}
	public String getDeviceIp() {
		return deviceIp;
	}
	public void setDeviceIp(String deviceIp) {
		this.deviceIp = deviceIp;
	}
	public String getDeviceType() {
		return deviceType;
	}
	public void setDeviceType(String deviceType) {
		this.deviceType = deviceType;
	}
	public String getDeviceStatus() {
		return deviceStatus;
	}
	public void setDeviceStatus(String deviceStatus) {
		this.deviceStatus = deviceStatus;
	}
	public String getDeviceVolume() {
		return deviceVolume;
	}
	public void setDeviceVolume(String deviceVolume) {
		this.deviceVolume = deviceVolume;
	}
	public String getDeviceOtherInfo() {
		return deviceOtherInfo;
	}
	public void setDeviceOtherInfo(String deviceOtherInfo) {
		this.deviceOtherInfo = deviceOtherInfo;
	}
	
	
}
